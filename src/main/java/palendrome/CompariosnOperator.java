public class CompariosnOperator {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int x = 100 , y = 200;



		// Equal to

		System.out.println("x == y = "+ (x==y) );

		// Not equal to
		System.out.println("x != y = "+ (x!=y) );

		//Less than
		System.out.println("x < y = "+ (x<y) );

		//Less than
		System.out.println("x > y = "+ (x>y) );

		//Greater than or equal to
		System.out.println("x >= y = " + (x >= y) );

		//Less than or equal to
		System.out.println("x <= y = " + (x <= y) );

		

		}

}
